package com.ejercicios.ejercicio1;

public class Empleado {
    
    String dni;
    String nombre;
    String apellidos;
    int edad;

    public Empleado(String dni, String nombre, String apellidos, int edad) {
        this.dni = dni;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.edad = edad;
    }
    
}
